          <p>“Madres Walking to Health” focuses on supporting and expanding upon a walking group that already exists within the <em>Madre a Madre</em> group at Washington Elementary. The group currently has relatively low participation and only meets twice a month. Chyu and Saxton hope to not only help grow this program, but also to help extend the collaborative, community spirit among the parents of Washington Elementary to focus on health and well-being.</p>

<p>Parents in the <em>Madre a Madre</em> group at Washington have expressed their interest in increasing physical activity for themselves as well as their children. Walking groups are a relatively simple, cost-effective way for parents to promote physical activity and have consistently demonstrated significant health benefits. A neighborhood assessment by the Santa Clara County Public Health Department also revealed that most streets in the neighborhood are well lit with easily accessible sidewalks and crosswalks, making the neighborhood a relatively safe location for walking. After mothers identify the safest walking routes, participating Santa Clara University students will help create maps and collaborate with mothers on a regular walking schedule of at least 2-3 sessions per week. Participants who agree to commit to the program for at least three months will be given pedometers to help log their steps. The goal is that following the 18-month grant, the walking group activities will be managed and sustained by the parents of the <em>Madres a Madres</em> group at Washington Elementary.</p>


